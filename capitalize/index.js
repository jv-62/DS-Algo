// Write a function that returns the provided string with
// the first letter of each word capitalized. Make sure the
// rest of the word is in lower case.
// --- Examples
//   capitalize("I'm a little tea pot") --> 'I'm A Little Tea Pot'
//   capitalize('sHoRt AnD sToUt') --> 'Short And Stout'

function capitalize(str) {
  // <--- Method 1 --->
  // let words = str.split(' ');
  // for (let i = 0; i < words.length; i++) {
  //   let wordsArray = words[i].split('');
  //   for (let j = 1; j < wordsArray.length; j++) {
  //     wordsArray[j] = wordsArray[j].toLowerCase();
  //   }
  //   wordsArray[0] = wordsArray[0].toUpperCase();
  //   words[i] = wordsArray.join('');
  // }
  // return words.join(' ');
  // <--- Method 2 --->
  let arrOfWords = str.split(' ');
  let arrOfWordsCase = [];
  for (let i = 0; i < arrOfWords.length; i++) {
    const word = arrOfWords[i];
    arrOfWordsCase.push(word[0].toUpperCase() + word.slice(1).toLowerCase());
  }
  return arrOfWordsCase.join(' ');
}

